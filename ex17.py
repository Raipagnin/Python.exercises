#calcule a buceta duma hipotenusa... caralho que odio formula a2 + b2 = h2
from math import sqrt
a = float(input('Digite o valor do primeiro cateto: '))
b = float(input('Digite o valor do segundo cateto: '))
h = sqrt(a*a + b*b)
print('a hipotenusa eh {:.2f}'.format(h))
''' existe o codigo hypot que eh exatamente a hypotenusa entao o codigo pode ser hypot(a, b)'''



